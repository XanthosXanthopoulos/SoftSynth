namespace Synth
{
    /// <summary>
    /// Styles of page animations for appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,

        /// <summary>
        /// The page expands and fades out
        /// </summary>
        ShrinkAndFadeOut = 1,

        /// <summary>
        /// The page shrinks and fades in
        /// </summary>
        ExpandAndFadeIn = 2
    }
}
